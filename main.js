const colors = {
  0: null,
  1: '#7ec8a0', // 北海道 - ミントグリーン
  2: '#f4a261', // 本州 - テラコッタオレンジ
  3: '#a8dadc', // 四国 - スカイブルー
  4: '#e76f51', // 九州 - サーモン
};

const table = document.getElementById('grid');
for (let r = 0; r < 16; r++) {
  const tr = document.createElement('tr');
  for (let c = 0; c < 16; c++) {
    const td = document.createElement('td');
    td.dataset.r = r;
    td.dataset.c = c;
    const color = colors[map[r][c]];
    if (color) td.style.background = color;
    tr.appendChild(td);
  }
  table.appendChild(tr);
}
